    public static function calcPriorityByCourse()
    {
        $prioridades = [];
        foreach(self::$courses as $key=>$value){
            print("Carregando reservas da disciplina ".$key."...\n");

            $reservas = DB::table("RESERVA")->select("nome as sala")
                ->join("REQUISICAO","RESERVA.requisicao_id","=","REQUISICAO.id")
                ->join("SALA","RESERVA.sala_numero","=","SALA.numero")
                ->where("atividade","LIKE","%".$value."%")->get()->groupBy('sala');

            print("Reservas da disciplina ".$key." carregadas.\n");

            if(count($reservas)>0){
                print("Calculando prioridades da disciplina ".$key."...\n");
                foreach($reservas as $key2=>$value2){
                    $prioridades[$key][$key2]=count($value2);
                }
                
                $total = array_sum($prioridades[$key]);
                if($total){
                    foreach($prioridades[$key] as $key2=>$value2){
                        $prioridades[$key][$key2] = 100*$value2/$total;
                    }
                }
                print("Prioridades da disciplina ".$key." calculadas.\n");
            }else{
                print("Não foram encontradas reservas para disciplina ".$key.".\n");
            }
        }
        return $prioridades;
    }

    public static function mergePrioritiesFromUrano($prioritiesPlus, $prioritiesMinus)
    {
        $disciplines = [];
        
        foreach(array_column($prioritiesPlus, "coddis") as $ds){
            foreach($ds as $d){
                array_push($disciplines, $d);
            }
        }

        foreach(array_column($prioritiesMinus, "coddis") as $ds){
            foreach($ds as $d){
                array_push($disciplines, $d);
            }
        }

        $disciplines = array_unique($disciplines);
        $priorities = [];

        foreach($disciplines as $discipline){
            $turmas = [];

            foreach($prioritiesPlus as $priority){
                if(in_array($discipline, $priority["coddis"])){
                    if(in_array("codtur", array_keys($priority))){
                        array_push($turmas,$priority["codtur"]);
                    }
                }
            }
            
            foreach($prioritiesMinus as $priority){
                if(in_array($discipline, $priority["coddis"])){
                    if(in_array("codtur", array_keys($priority))){
                        array_push($turmas,$priority["codtur"]);
                    }
                }
            }

            $turmas = array_unique($turmas);
            $priorities[$discipline] = [];

            foreach($turmas as $turma){
                $salas = [];

                foreach($prioritiesPlus as $priority){
                    if(in_array($discipline, $priority["coddis"])){
                        if($turma == $priority["codtur"]){
                            array_push($salas, $priority["salas"]);
                        }
                    }
                }

                foreach($prioritiesMinus as $priority){
                    if(in_array($discipline, $priority["coddis"])){
                        if($turma == $priority["codtur"]){
                            array_push($salas, $priority["salas"]);
                        }
                    }
                }

                $keys = [];
                
                foreach($salas as $array){
                    foreach($array as $key=>$value){
                        array_push($keys,$key);
                    }
                }

                $keys = array_unique($keys);

                $priorities[$discipline][$turma] = [];
                
                foreach($keys as $sala){
                    $priorities[$discipline][$turma][$sala] = 0;
                    foreach($salas as $array){
                        foreach($array as $key=>$value){
                            if($key==$sala){
                                $priorities[$discipline][$turma][$sala] += $value;
                            }
                        }
                    }
                }
            }
        }

        return $priorities;
    }

